<script>
import { mapActions, mapState } from 'vuex'
import { TimelineMax, TweenMax } from 'gsap'

export default {
  name: 'Home',
  data () {
    return {
      timeline: new TimelineMax(),
      transitionName: ''
    }
  },
  computed: {
    ...mapState({
      selectedCity: state => state.city.selected
    })
  },
  mounted () {
    let citiesLocal = JSON.parse(localStorage.getItem('cities'))
    let cityName = this.$route.params.cityName
    let selectedCity = {}

    if (citiesLocal && citiesLocal.list && cityName) {
      selectedCity = citiesLocal.list.filter(item => {
        return cityName === item.name.toLowerCase()
      })
      this.setSelectedCity(selectedCity)
    }

    this.timeline.add(this.tlResetAll())

    setTimeout(() => {
      this.randomiseWeather()
    }, 1000)
  },
  watch: {
    '$route' (to, from) {
      const toDepth = to.path.split('/').length
      const fromDepth = from.path.split('/').length
      this.transitionName = toDepth < fromDepth ? 'view-out' : 'view-in'
    },
    selectedCity (val) {
      const weather = val['weather']

      if (weather && weather.length) {
        this.timeline.clear()
        this.timeline.add([
          this.tlClearClouds(),
          this.tweenGround(),
          this.tweenSkyDark()
        ])

        for (let i = 0; i < weather.length; ++i) {
          const id = weather[i]['id']

          switch (parseInt(id.toString()[0])) {
            case 3: {
              if (id >= 302) {
                this.timeline.add([
                  this.tweenSkyDark()
                ])
              } else {
                this.timeline.add(this.tweenSkyDark())
              }
              break
            }
            case 5: {
              this.timeline.add(this.tlWeatherRain())
              break
            }
            case 6: {
              this.timeline.add(this.tlWeatherSnow())
              break
            }
            case 8: {
              this.timeline.add([
                this.tweenSunSmall(true)
              ])
              this.timeline.add([
                this.tlClearClouds(),
                this.tweenSunBig(),
                this.tweenSkyBright()
              ])

              if (id > 800 && id < 803) {
                this.timeline.add([
                  this.tweenCloudsNormal(),
                  this.tweenSunBig(),
                  this.tweenSkyBright()
                ])
              } else if (id >= 803) {
                this.timeline.add([
                  this.tweenCloudsWind(),
                  this.tweenSunBig(),
                  this.tweenSkyBright()
                ])
              }
              break
            }
            default: {
              this.timeline.add(this.tlWeatherNeutral())
            }
          }
        }
      } else {
        this.randomiseWeather()
      }
    }
  },
  methods: {
    ...mapActions({
      'setSelectedCity': 'city/setSelected',
      'clearSelectedCity': 'city/clearSelected'
    }),
    randomiseWeather () {
      let rand = Math.round(Math.random() * 10)

      rand = 7

      switch (rand) {
        case 1: {
          this.timeline.add([
            this.tweenGround(),
            this.tweenSkyBright(),
            this.tweenCloudsWind()
          ])
          break
        }
        case 2: {
          this.timeline.add([
            this.tweenGround(),
            this.tweenSkyDark(),
            this.tweenCloudsNormal()
          ])
          break
        }
        case 3: {
          this.timeline.add([
            this.tweenGround(),
            this.tweenGroundSnow(),
            this.tweenCloudsSnow(),
            this.tweenSkyDark(),
            this.tweenSunSmall()
          ])
          break
        }
        case 4: {
          this.timeline.add([
            this.tweenGround(),
            this.tweenGroundSnow(),
            this.tweenSkyDark(),
            this.tweenSunSmall()
          ])
          break
        }
        case 5: {
          this.timeline.add(this.tlWeatherRain())
          break
        }
        case 6: {
          this.timeline.add(this.tlWeatherNeutral())
          break
        }
        case 7: {
          this.timeline.add([
            this.tweenGround(),
            this.tweenSkyBright()
          ])
          break
        }
        case 8: {
          this.timeline.add([
            this.tweenGround(),
            this.tweenSkyBright(),
            this.tweenCloudsNormal()
          ])
          break
        }
        case 9: {
          this.timeline.add([
            this.tweenGround(),
            this.tweenSkyDark(),
            this.tweenCloudsWind()
          ])
          break
        }
        case 10: {
          this.timeline.add([
            this.tweenGround(),
            this.tweenSkyDark(),
            this.tweenSunSmall()
          ])
        }
      }
    },
    tweenSkyBright (set = false) {
      const selector = '#Sky'

      if (set) {
        return TweenMax
          .set(selector, {
            opacity: 0,
            fill: '#F2B770'
          })
      }

      return TweenMax
        .to(selector, 0.25, {
          opacity: 1,
          fill: '#F2B770'
        })
    },
    tweenSkyDark (set = false) {
      const selector = '#Sky'

      if (set) {
        return TweenMax
          .set(selector, {
            opacity: 0,
            fill: '#6E93E5'
          })
      }

      return TweenMax
        .to(selector, 0.25, {
          opacity: 1,
          fill: '#6E93E5'
        })
    },
    tweenSunSmall (set = false) {
      const selector = '#Sun_small'

      if (set) {
        return TweenMax
          .set(selector, {
            opacity: 0
          })
      } else {
        this.timeline.add(this.tweenSunBig(true))
      }

      return TweenMax
        .to(selector, 0.25, {
          opacity: 1
        })
    },
    tweenSunBig (set = false) {
      const selector = '#Sun_big'

      if (set) {
        return TweenMax
          .set(selector, {
            opacity: 0,
            transformOrigin: 'center center'
          })
      } else {
        this.timeline.add(this.tweenSunSmall(true))
      }

      this.timeline.add(
        TweenMax
          .to(selector, 0.25, {
            opacity: 1,
            transformOrigin: 'center center'
          })
      )

      let tl = new TimelineMax({
        repeat: -1,
        yoyo: true
      })

      tl.add([
        TweenMax.fromTo(
          selector, 1, {
            scale: 1
          }, {
            scale: 1.1
          }
        )
      ])

      return tl
    },
    tweenCloudsNormal (set = false) {
      const selector = '#Clouds_normal'

      if (set) {
        return TweenMax
          .set(selector, {
            opacity: 0
          })
      } else {
        this.timeline.add([
          this.tweenCloudsRain(true),
          this.tweenCloudsSnow(true),
          this.tweenCloudsWind(true)
        ])
      }

      this.timeline.add(
        TweenMax
          .to(selector, 0.25, {
            opacity: 1
          })
      )

      let tl = new TimelineMax({
        repeat: -1,
        yoyo: true,
        ease: Power1.easeInOut
      })

      tl.add(
        TweenMax.fromTo(
          selector, 5, {
            x: -25
          }, {
            x: 25
          }
        )
      )

      return tl
    },
    tweenCloudsRain (set = false) {
      const selector = '#Clouds_rain'

      if (set) {
        return TweenMax
          .set(selector, {
            opacity: 0
          })
      } else {
        this.timeline.add([
          this.tweenCloudsNormal(true),
          this.tweenCloudsSnow(true),
          this.tweenCloudsWind(true)
        ])
      }

      this.timeline.add(
        TweenMax
          .to(selector, 0.25, {
            opacity: 1
          })
      )

      let tl = new TimelineMax({
        repeat: -1,
        yoyo: true
      })

      tl.add(
        TweenMax.fromTo(
          '.cloud-rain-drop', 0.25, {
            x: 10
          }, {
            x: 0
          }
        )
      )
      tl.add([
        TweenMax.fromTo(
          '#Flash', 0.25, {
            transformOrigin: 'top center',
            scale: 1
          }, {
            scale: 0.75
          }
        ),
        TweenMax.fromTo(
          '.cloud-rain-cloud', 0.25, {
            fill: '#666'
          }, {
            fill: '#D1CFBD'
          }
        ),
        '+=1'
      ])

      return tl
    },
    tweenCloudsSnow (set = false) {
      const selector = '#Clouds_snow'

      if (set) {
        return TweenMax
          .set(selector, {
            opacity: 0
          })
      } else {
        this.timeline.add([
          this.tweenCloudsNormal(true),
          this.tweenCloudsRain(true),
          this.tweenCloudsWind(true)
        ])
      }

      this.timeline.add(
        TweenMax
          .to(selector, 0.25, {
            opacity: 1
          })
      )

      if (this.cloudsSnowTl) {
        this.cloudsSnowTl.clear()
      }

      let tl = new TimelineMax({
        repeat: -1,
        yoyo: true
      })

      tl.add(
        TweenMax.fromTo('.snow-fall', 1, {
          y: 10,
          ease: Power1.easeOut
        }, {
          y: -5,
          ease: Power1.easeIn
        })
      )

      return tl
    },
    tweenCloudsWind (set = false) {
      const selector = '#Clouds_wind'

      if (set) {
        return TweenMax
          .set(selector, {
            opacity: 0
          })
      } else {
        this.timeline.add([
          this.tweenCloudsNormal(true),
          this.tweenCloudsRain(true),
          this.tweenCloudsSnow(true)
        ])
      }

      this.timeline.add(
        TweenMax
          .to(selector, 0.25, {
            opacity: 1
          })
      )

      let tl = new TimelineMax({
        repeat: -1
      })
      const cloudsClouds = '#Clouds_wind_clouds'
      const cloudsBreezes = '#Clouds_wind_breezes'

      tl.add([
        TweenMax.fromTo(cloudsClouds, 2, {
          opacity: 0,
          x: -50
        }, {
          opacity: 1,
          x: 0
        }),
        TweenMax.fromTo(cloudsBreezes, 2, {
          opacity: 0,
          x: -200
        }, {
          opacity: 1,
          x: -100
        })
      ])
      tl.add([
        TweenMax.fromTo(cloudsClouds, 3, {
          x: 0
        }, {
          x: 100
        }),
        TweenMax.fromTo(cloudsBreezes, 3, {
          x: -100
        }, {
          x: 100
        })
      ])
      tl.add([
        TweenMax.fromTo(cloudsClouds, 2, {
          opacity: 1,
          x: 100
        }, {
          opacity: 0,
          x: 200
        }),
        TweenMax.fromTo(cloudsBreezes, 2, {
          opacity: 1,
          x: 100
        }, {
          opacity: 0,
          x: 200
        })
      ])

      return tl
    },
    tweenGround (set = false) {
      const selector = '#Ground'
      let tl = new TimelineMax()

      if (set) {
        return TweenMax
          .set(selector, {
            opacity: 0
          })
      }

      tl.add([
        TweenMax
          .to('.ground-snow-pattern', 0.25, {
            opacity: 0,
            fill: '#69AF7E'
          }),
        TweenMax
          .to(selector, 0.25, {
            opacity: 1
          })
      ])

      return tl
    },
    tweenGroundSnow (set = false) {
      const selector = '.ground-snow-pattern'

      if (set) {
        return TweenMax
          .set(selector, {
            opacity: 0,
            fill: '#69AF7E'
          })
      }

      return TweenMax
        .to(selector, 0.25, {
          opacity: 1,
          fill: '#ffffff'
        })
    },
    tlResetAll () {
      return [
        this.tweenSkyBright(true),
        this.tweenSkyDark(true),
        this.tweenSunSmall(true),
        this.tweenSunBig(true),
        this.tweenCloudsRain(true),
        this.tweenCloudsWind(true),
        this.tweenCloudsNormal(true),
        this.tweenCloudsSnow(true),
        this.tweenGround(true),
        this.tweenGroundSnow(true)
      ]
    },
    tlClearClouds () {
      return [
        this.tweenCloudsRain(true),
        this.tweenCloudsWind(true),
        this.tweenCloudsNormal(true),
        this.tweenCloudsSnow(true)
      ]
    },
    tlWeatherNeutral () {
      return [
        this.tweenCloudsRain(true),
        this.tweenCloudsWind(true),
        this.tweenCloudsNormal(true),
        this.tweenCloudsSnow(true),
        this.tweenGround(),
        this.tweenSkyDark(),
        this.tweenSunSmall()
      ]
    },
    tlWeatherRain () {
      return [
        this.tweenGround(),
        this.tweenCloudsRain(),
        this.tweenSkyDark(),
        this.tweenSunSmall()
      ]
    },
    tlWeatherSnow () {
      return [
        this.tweenGroundSnow(),
        this.tweenCloudsSnow(),
        this.tweenSkyDark(),
        this.tweenSunSmall()
      ]
    }
  }
}
</script>

